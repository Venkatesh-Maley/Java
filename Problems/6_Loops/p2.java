// LOOPS - SERIES PRINTING
// 2. Print numbers from N to 1 and 1 to N using a while loop.

class p2 {
    public static void main (String[] args){
        int N = 20;

        while (N >= 1){
            System.out.println(N);
            N --;
        }

        int n = 20;
        int i = 1;

        while (i <= n){
            System.out.println(i);
            i++;
        }
    }
}
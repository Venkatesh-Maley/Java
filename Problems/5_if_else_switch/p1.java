// IF ELSE SWITCH - Basic conditions
// 1. Read a number and check whether it is positive, negative, or zero.

class p1{
    public static void main (String [] args){
        int num = 12;

        if (num < 0){
            System.out.println("Given number is a Negative number");
        }
        else if (num > 0){
            System.out.println("Given number is a Positive number");
        }
        else {
            System.out.println("Given number is Zero");
        }
    }
}
// 3. Check whether a number is divisible by 3.

class p3{
    public static void main (String[] args){
        int num = 12;

        if(num%3 ==0){
            System.out.println("Given number is divisible by 3");
        }
        else{
            System.out.println("Given number is not divisible by 3");
        }
    }
}

// IF ELSE SWITCH - Basic conditions
// 2. Read an integer and check whether it is even or odd.

class p2 {
    public static void main(String[] args) {
        int num = 11;

        if (num % 2 == 0) {
            System.out.println("Even number");
        } else {
            System.out.println("Odd number");
        }
    }
}

